# One statement per line (preferred)
print("Python is fun!")
print("Hello World!")
print("Have a good day.")
print("Learning Python is fun!")

# Multiple statements on one line (allowed; use sparingly)
print("Hello"); print("How are you?"); print("Bye bye!")

